#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    int iNum1, iNum2;
    int iVal1, iVal2;
    printf("Input the first integer number\r\n");
    scanf_s("%d", &iNum1);
    printf("Input the second integer number\r\n");
    scanf_s("%d", &iNum2);
    iVal1 = abs(iNum1);
    iVal2 = abs(iNum2);
    if (iVal1 > 0 && iVal2 > 0)
    {
        int nGcd;
        while (iVal1 != iVal2)
        {
            if (iVal1 > iVal2)
                iVal1 -= iVal2;
            else
                iVal2 -= iVal1;
        }
        nGcd = iVal1;
        printf("The Gcd of the numbers %d and %d is %d\r\n", iNum1, iNum2, nGcd);
    }
    else
    {
        printf("The numbers should not be equal 0\r\n");
    }
    _fgetchar();
    fgetc(stdin);
}

